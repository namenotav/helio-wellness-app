export const getMeditationsByCategory = (category) => {
  return meditationLibrary.filter(med => med.category === category)
}

export const getMeditationsByDuration = (maxMinutes) => {
  return meditationLibrary.filter(med => {
    const duration = parseInt(med.duration)
    return duration <= maxMinutes
  })
}

export const getBeginnerMeditations = () => {
  return meditationLibrary.filter(med => med.difficulty === 'Beginner')
}
